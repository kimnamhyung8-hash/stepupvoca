export const getBrowserLanguage = (): LanguageCode => {
    if (typeof navigator === 'undefined') return 'en';
    const rawLang = navigator.language || (navigator as any).userLanguage || 'en';
    if (translations[rawLang as LanguageCode]) return rawLang as LanguageCode;
    const shortLang = rawLang.split('-')[0];
    if (translations[shortLang as LanguageCode]) return shortLang as LanguageCode;
    return 'en';
};

export const t = (lang: string | undefined | null, key: string): string => {
    const l = (lang || 'en') as LanguageCode;
    return translations[l]?.[key] || translations['en']?.[key] || key;
};
