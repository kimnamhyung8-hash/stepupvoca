export function tInfo(lang: string, key: string, params?: Record<string, string | number>): string {
  const dict = infoDict[lang] || infoDict['en'] || {};
  let text = dict[key] || infoDict['en']?.[key] || key;
  
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
